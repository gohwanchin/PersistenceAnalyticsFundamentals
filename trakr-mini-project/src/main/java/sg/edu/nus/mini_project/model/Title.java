package sg.edu.nus.mini_project.model;

import jakarta.json.JsonObject;

public class Title {
    private String title;
    private String year;
    private String rated;
    private String released;
    private String runtime;
    private String genre;
    private String director;
    private String writer;
    private String actors;
    private String plot;
    private String language;
    private String country;
    private String awards;
    private String poster;
    private String metascore;
    private String imdbRating;
    private String imdbVotes;
    private String totalSeasons;
    private String boxOffice;
    private String id;
    private String type;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getRated() {
        return rated;
    }

    public void setRated(String rated) {
        this.rated = rated;
    }

    public String getReleased() {
        return released;
    }

    public void setReleased(String released) {
        this.released = released;
    }

    public String getRuntime() {
        return runtime;
    }

    public void setRuntime(String runtime) {
        this.runtime = runtime;
    }

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }

    public String getDirector() {
        return director;
    }

    public void setDirector(String director) {
        this.director = director;
    }

    public String getWriter() {
        return writer;
    }

    public void setWriter(String writer) {
        this.writer = writer;
    }

    public String getActors() {
        return actors;
    }

    public void setActors(String actors) {
        this.actors = actors;
    }

    public String getPlot() {
        return plot;
    }

    public void setPlot(String plot) {
        this.plot = plot;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getAwards() {
        return awards;
    }

    public void setAwards(String awards) {
        this.awards = awards;
    }

    public String getPoster() {
        return poster;
    }

    public void setPoster(String poster) {
        this.poster = poster;
    }

    public String getMetascore() {
        return metascore;
    }

    public void setMetascore(String metascore) {
        this.metascore = metascore;
    }

    public String getImdbRating() {
        return imdbRating;
    }

    public void setImdbRating(String imdbRating) {
        this.imdbRating = imdbRating;
    }

    public String getImdbVotes() {
        return imdbVotes;
    }

    public void setImdbVotes(String imdbVotes) {
        this.imdbVotes = imdbVotes;
    }

    public String getTotalSeasons() {
        return totalSeasons;
    }

    public void setTotalSeasons(String totalSeasons) {
        this.totalSeasons = totalSeasons;
    }

    public String getBoxOffice() {
        return boxOffice;
    }

    public void setBoxOffice(String boxOffice) {
        this.boxOffice = boxOffice;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public static Title create(JsonObject obj) {
        Title t = new Title();
        t.setTitle(obj.getString("Title"));
        t.setYear(obj.getString("Year"));
        t.setRated(obj.getString("Rated"));
        t.setRuntime(obj.getString("Runtime"));
        t.setGenre(obj.getString("Genre"));
        t.setDirector(obj.getString("Director"));
        t.setWriter(obj.getString("Writer"));
        t.setActors(obj.getString("Actors"));
        t.setPlot(obj.getString("Plot"));
        t.setLanguage(obj.getString("Language"));
        t.setCountry(obj.getString("Country"));
        t.setAwards(obj.getString("Awards"));
        t.setPoster(obj.getString("Poster"));
        t.setImdbRating(obj.getString("imdbRating"));
        t.setImdbVotes(obj.getString("imdbVotes"));
        t.setMetascore(obj.getString("Metascore"));
        t.setId(obj.getString("imdbID"));
        t.setType(obj.getString("Type"));
        if (obj.getString("Type").equals("movie"))
            t.setBoxOffice(obj.getString("BoxOffice"));
        else
            t.setTotalSeasons(obj.getString("totalSeasons"));
        return t;
    }
}
